package it.jait54.geom.model;

public class InputObject {
	String formaGeometrica;
	int base;
	int altezza;
	int lato1;
	int lato2;
	
	
	public String getFormaGeometrica() {
		return formaGeometrica;
	}
	public void setFormaGeometrica(String formaGeometrica) {
		this.formaGeometrica = formaGeometrica;
	}
	public int getBase() {
		return base;
	}
	public void setBase(int base) {
		this.base = base;
	}
	public int getAltezza() {
		return altezza;
	}
	public void setAltezza(int altezza) {
		this.altezza = altezza;
	}
	public int getLato1() {
		return lato1;
	}
	public void setLato1(int lato1) {
		this.lato1 = lato1;
	}
	public int getLato2() {
		return lato2;
	}
	public void setLato2(int lato2) {
		this.lato2 = lato2;
	}

	
}
